    private void xacnhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xacnhanActionPerformed
        // TODO add your handling code here:
        veDTO ve=new veDTO();
        phuongtienDTO veh=new phuongtienDTO();
        hoadonDTO hd=new hoadonDTO();
        veh.setBiensoXe(bienso.getText());
        veh.setLoaiXe(loaixe.getText());
        String var1=timein.getText();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm");
        LocalDateTime dateTime1 = LocalDateTime.parse(var1, formatter);
        veh.setTimeIn(dateTime1);
        String var2=timeout.getText();
        LocalDateTime dateTime2 = LocalDateTime.parse(var2, formatter);
        hd.setTimeOut(dateTime2);
        ve.setIDVe(mave.getText());
        ve.setLoaiVe(loaive.getText());
        hd.setThanhTien(Integer.parseInt(thanhtien.getText()));
        ss.nhapHoaDon(ve, veh, hd);
        if (loaive.getText().equals("Vé ngày   ")) ss.traVe(mave.getText());
        else ss.traVeThang(mave.getText());
        ss.xoaXe(mave.getText());
        if (xoa==1){
            ss.xoaHinhAnh(bienso.getText());
            xoa=0;
        }
        JFrame frame = new JFrame("Key Press Example");
        frame.setSize(300, 200);
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        JOptionPane.showMessageDialog(frame, "Xe đã rời khỏi bãi!");
        this.dispose();
    }//GEN-LAST:event_xacnhanActionPerformed

