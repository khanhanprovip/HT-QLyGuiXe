    public int tinhtien(){
        int thanhtoan=0;
        //System.out.print(loaixe.getText());
        //System.out.print(loaixe.getText());
        if (loaive.getText().equals("Vé tháng  ")) {thanhtoan=0;}
        if (loaive.getText().equals("Vé ngày   ")){
            //System.out.print(loaixe.getText());
          
            if (loaixe.getText().equals("Xe máy    ")){
                 thanhtoan=thanhtoan+5000;
                 
                  
            }
            if (loaixe.getText().equals("Ô tô      ")){
                 thanhtoan+=10000;
            }
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH");
            int gio=Integer.parseInt(LocalDateTime.now().format(formatter));
            if (gio>=22||gio<=6){
                 thanhtoan+=10000;
        }
        }
        
        int check=ss.KtViPham(bienso.getText());
        if (check==1) {
            danchung.setEnabled(true);
            vitri.setForeground(Color.decode("#FF0000"));
            vitri.setText("Xe sai vị trí! Phạt 50000");
            thanhtoan+=50000;
            xoa=1;
        }
        return thanhtoan;
    }

